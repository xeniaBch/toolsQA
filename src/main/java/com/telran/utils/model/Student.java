package com.telran.utils.model;

public class Student {
   private String firtsname;
   private String lastname;
   private String email;
   private String phone;
   private String path;
   private String address;
   private String day;
   private String month;
   private String year;

    public String getFirtsname() {
        return firtsname;
    }

    public Student setFirtsname(String firtsname) {
        this.firtsname = firtsname;
        return this;
    }

    public String getLastname() {
        return lastname;
    }

    public Student setLastname(String lastname) {
        this.lastname = lastname;
        return this;
    }

    public String getEmail() {
        return email;
    }

    public Student setEmail(String email) {
        this.email = email;
        return this;
    }

    public String getPhone() {
        return phone;
    }

    public Student setPhone(String phone) {
        this.phone = phone;
        return this;
    }

    public String getPath() {
        return path;
    }

    public Student setPath(String path) {
        this.path = path;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public Student setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getDay() {
        return day;
    }

    public Student setDay(String day) {
        this.day = day;
        return this;
    }

    public String getMonth() {
        return month;
    }

    public Student setMonth(String month) {
        this.month = month;
        return this;
    }

    public String getYear() {
        return year;
    }

    public Student setYear(String year) {
        this.year = year;
        return this;
    }
}
